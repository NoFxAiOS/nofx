// parseEventTime è§£æäº‹ä»¶æ—¥æœŸå’Œæ—¶é—´ (æ ¼å¼: "dd/mm/yyyy" å’Œ "HH:MM")
func parseEventTime(date, eventTime string) (time.Time, error) {
	// å¦‚æœæ—¶é—´æ˜¯"å…¨å¤©"æˆ–"å¾…å®š",é»˜è®¤è®¾ç½®ä¸ºå½“å¤©çš„12:00
	if eventTime == "å…¨å¤©" || eventTime == "å¾…å®š" || eventTime == "" {
		eventTime = "12:00"
	}

	// ç»„åˆæ—¥æœŸå’Œæ—¶é—´
	dateTimeStr := date + " " + eventTime
	return time.Parse("02/01/2006 15:04", dateTimeStr)
}

// formatTimeUntil æ ¼å¼åŒ–"è·ç¦»ç°åœ¨çš„æ—¶é—´"
func formatTimeUntil(eventTime, now time.Time) string {
	duration := eventTime.Sub(now)

	if duration < time.Hour {
		minutes := int(duration.Minutes())
		return fmt.Sprintf("%dåˆ†é’Ÿå", minutes)
	} else if duration < 24*time.Hour {
		hours := int(duration.Hours())
		minutes := int(duration.Minutes()) % 60
		if minutes > 0 {
			return fmt.Sprintf("%då°æ—¶%dåˆ†é’Ÿå", hours, minutes)
		}
		return fmt.Sprintf("%då°æ—¶å", hours)
	} else {
		days := int(duration.Hours() / 24)
		hours := int(duration.Hours()) % 24
		if hours > 0 {
			return fmt.Sprintf("%då¤©%då°æ—¶å", days, hours)
		}
		return fmt.Sprintf("%då¤©å", days)
	}
}
